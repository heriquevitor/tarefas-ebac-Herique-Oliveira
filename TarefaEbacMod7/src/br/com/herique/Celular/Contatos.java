package br.com.herique.Celular;

/**
 * @author herique.oliveira
 * @version 1.0
 * <br> Classe criada para retornar quem está ligado com a mensagem mostrando nome,id, numero
 *
 */

public class Contatos {
    private String name;
    private int id;
    private int number;
    /**
     * @return getName retorna um nome para representar quem esta ligando.
     * @class br.com.herique.Contatos
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public String getName() {
        return name;
    }
    /**
     * @return O setName retorna uma mensagem com o nome do contato
     * @class br.com.herique.Chamada
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public void setName(String name) {
        this.name = name;
    }
    /**
     * @return getId retorna um id para representar quem esta ligando.
     * @class br.com.herique.Contatos
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public int getId() {
        return id;
    }
    /**
     * @return O setId retorna uma mensagem com id do contato
     * @class br.com.herique.Chamada
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public void setId(int id) {
        this.id = id;
    }


    /**
     * @return getNumber retorna numero de quem está ligando
     * @class br.com.herique.Contatos
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public int getNumber() {
        return number;
    }
    /**
     * @return O setId retorna uma mensagem com numero do contato
     * @class br.com.herique.Chamada
     * @see br.com.herique.Celular.Contatos
     *
     * */
    public void setNumber(int number) {
        this.number = number;
    }













}
