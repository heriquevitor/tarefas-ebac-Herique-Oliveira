package br.com.herique.Celular;

/**
 * @author herique.oliveira
 * @version 1.0
 * <br> Classe criada para representar quando o celular esta ligado ou desligado e exibir uma mensagem
 *
 */

public class LigaDesliga {
    private String liga;
    private String desliga;

    /**
    * @return O liga  representando se está ligado o celular
     * @class br.com.herique.LigaDesliga
     * @see br.com.herique.Celular.LigaDesliga
     *
     * */
    public String getLiga() {
        return liga;
    }
    /**
     * @return setLiga  retorna a mensagem quando estiver ligado o celular
     * @class br.com.herique.LigaDesliga
     * @see br.com.herique.Celular.LigaDesliga
     *
     * */
    public void setLiga(String liga) {
        this.liga = liga;
    }
    /**
     * @return getDesliga  representa se está desligado o celular
     * @class br.com.herique.LigaDesliga
     * @see br.com.herique.Celular.LigaDesliga
     *
     * */
    public String getDesliga() {
        return desliga;
    }
    /**
     * @return setDesliga  retorna uma mensagem quando o celular estiver desligado
     * @class br.com.herique.LigaDesliga
     * @see br.com.herique.Celular.LigaDesliga
     *
     * */
    public void setDesliga(String desliga) {
        this.desliga = desliga;
    }





    }

